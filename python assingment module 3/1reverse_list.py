#  Que1 :- What is List? How will you reverse a list?

'''Ans :- A list is a data structure in python that is a mutable or changeable,ordered
       squence of elements 
'''       

# example:-

list = ["naresh","kamal","rohit" ]
print(list)

# by default function
list.reverse()
