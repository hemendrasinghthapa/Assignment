#  Que3 :- Differentiate between append () and extend () methods?

'''Ans : - append() : - The append() method is a built-in function used to 
                        add an item to the end of a list.  
'''                        
# example :-
  
list = ["C","C++","Java","Node"]
list.append("Python")
print(list)

# ---------------------------------------------------------------------------------

'''Ans : - extend() : - The extend() method is use to add multiple elements in a list.                          
'''                        

# exapmle : -

list = ["C++","Java","Python","Angular"]
list.extend(["Flutter","React"])
print(list)