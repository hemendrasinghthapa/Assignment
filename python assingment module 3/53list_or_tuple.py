# Que53:- How can you pick a random item from a list or tuple?

# Ans:-

import random

my_list = [1, 2, 3, 4, 5]
random_num = random.choice(my_list)
print("Random item from the list:", random_num)

t1 = (12,2,34,45,63,7)
random_num = random.choice(t1)
print("Random item from the tuple:", random_num)




