# Que2 : -How will you remove last object from a list? Suppose list1 is
#        [2, 33, 222, 14, and 25], what is list1 [-1]?

# example:-


list = [2,33,222,14,25]
list.pop()       # pop method by default removes last element in list.
print(list)



list1 = [2,33,222,14,25]
print(list1[-1])       # it will display last element of the list.

