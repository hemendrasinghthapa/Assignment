# Que14:- How many except statements can a try-except block have? Name Some built-in exception classes:

# Ans:-
'''
A try-except block can have multiple except statements to handle different types of exceptions.
There can be one except statement for each different type of exception you want to handle.

Here are some commonly used built-in exception classes in Python:

ZeroDivisionError: Raised when division or modulo by zero occurs.
FileNotFoundError: Raised when a file or directory is requested but cannot be found.
TypeError: Raised when an operation or function is applied to an object of an inappropriate type.
ValueError: Raised when a built-in operation or function receives an argument that has the right type but an inappropriate value.
IndexError: Raised when a sequence subscript is out of range.
KeyError: Raised when a dictionary key is not found.
NameError: Raised when a local or global name is not found.
SyntaxError: Raised when the parser encounters a syntax error.
IndentationError: Raised when there is incorrect indentation.
AssertionError: Raised when an assert statement fails.
AttributeError: Raised when attribute assignment or reference fails.
RuntimeError: Raised when a generated error does not fall into any category.
IOError: Raised when an input/output operation fails, such as the print statement or the open() function when trying to open a file that does not exist.
'''